# Python program to show shift operators
a = 10
b = -10

# print bitwise right shift operator
print("a >> 2 =", a >> 2) # a >> 1 = 5, a >> 2 = 2, a >> 3 = 1, a >> 4 = 0, a >> 5 = 0...
print("b >> 1 =", b >> 1)

# print bitwise left shift operator
print("a << 2 =", a << 2)
print("b << 1 =", b << 1)

